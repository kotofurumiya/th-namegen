// src/filter/index.ts
var oneOfTitleTags = (tags) => {
  return (character, title) => {
    return tags.some((t) => title.tags.includes(t));
  };
};
var noneOfTitleTags = (tags) => {
  return (character, title) => {
    return !tags.some((t) => title.tags.includes(t));
  };
};
var yearFrom = (year) => {
  return (character, title) => {
    return title.releasedYear ? title.releasedYear >= year : true;
  };
};
var yearTo = (year) => {
  return (character, title) => {
    return title.releasedYear ? title.releasedYear <= year : true;
  };
};
var selectCharacters = (filters = [], options = {}) => {
  const allCharacters2 = (options.titles || allTitleData).flatMap((title) => title.characters.map((character) => ({
    title,
    character
  })));
  const selected = filters.reduce((prev, f) => prev.filter((char) => f(char.character, char.title)), allCharacters2).map((char) => char.character);
  return selected;
};

// src/index.ts
var random = (min, max) => {
  return min + Math.floor(Math.random() * (max - min + 1));
};
var identity = (name) => name;
var pickFirstNameLike = (character) => {
  switch (character.nameType) {
    case "simple":
      return character.name;
    case "fullname":
      return character.firstName;
    default: {
      const check = character;
      return "";
    }
  }
};
var pickLastNameLike = (character) => {
  switch (character.nameType) {
    case "simple":
      return character.name;
    case "fullname":
      return character.lastName;
    default: {
      const check = character;
      return "";
    }
  }
};
var allCharacters = selectCharacters();
var generateName = (options = {}) => {
  var _a, _b, _c, _d;
  const delimiter = (_a = options.delimiter) != null ? _a : "-";
  const randomFunc = (_b = options.randomFunc) != null ? _b : random;
  const candidates = (_c = options.characterCandidates) != null ? _c : allCharacters;
  const converter = (_d = options.nameChunkConverter) != null ? _d : identity;
  if (candidates.length === 0) {
    throw new Error("`characterCandidates` is empty. It must contains some characters.");
  }
  const characterA = candidates[randomFunc(0, candidates.length - 1)];
  const characterB = candidates[randomFunc(0, candidates.length - 1)];
  const generated = [
    converter(pickFirstNameLike(characterA), {character: characterA, treatedAs: "firstName"}),
    converter(pickLastNameLike(characterB), {character: characterB, treatedAs: "lastName"})
  ].join(delimiter);
  return generated;
};
